'''
Password Validity Checker

Write a Python program to check the validity of passwords input by users.

Validation :

At least 1 letter between [a-z] and 1 letter between [A-Z].
At least 1 number between [0-9].
At least 1 character from [$#@].
Minimum length 6 characters.
Maximum length 16 characters.
'''

class PasswordValidator:
    def __init__(self, password):
        self.password = password
        self.special_chars = "$#@"

    def is_valid_length(self):
        return 6 <= len(self.password) <= 16

    def has_lowercase(self):
        return any(c.islower() for c in self.password)

    def has_uppercase(self):
        return any(c.isupper() for c in self.password)

    def has_digit(self):
        return any(c.isdigit() for c in self.password)

    def has_special(self):
        return any(c in self.special_chars for c in self.password)

    def is_valid(self):
        return all([
            self.is_valid_length(),
            self.has_lowercase(),
            self.has_uppercase(),
            self.has_digit(),
            self.has_special()
        ])

password = input("Enter password: ")
validator = PasswordValidator(password)
print("Valid Password" if validator.is_valid() else "Invalid Password")


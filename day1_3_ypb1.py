# Logical Operators
# AND OR NOT

# AND operator requires more than one condition
# AND operator returns TRUE only when both the conditions are TRUE

print(100==100 and 50<100)
#      cond1        cond2
#       true         true


print(100==100 and 50>100)
#      cond1        cond2
#       true         false

print(10==100 and 50>100)
#      cond1        cond2
#      false        false

# OR operator requires more than one condition
# OR operator returns TRUE when both the conditions are TRUE
# OR operator returns TRUE if any one of the conditions is TRUE
# OR operator returns FALSE when both the conditions are FALSE

print(100==100 or 50<100)
#      cond1        cond2
#       true         true


print(100==100 or 50>100)
#      cond1        cond2
#       true        false

print(10==100 or 50>100)
#      cond1        cond2
#      false        false

# NOT operator
# inverse the result
# True --> False     False --> True

